package com.javarush.task.jdk13.task04.task0438;

/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) {
        //напишите тут ваш код
        for (int i = 0; i <= 10; i++) {
            if (i == 0) {
                for (int j = 1; j < 10; j++) {
                    System.out.print(8);
                }
            }
            System.out.println(8);
        }

    }
}
